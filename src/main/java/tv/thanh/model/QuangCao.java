package tv.thanh.model;

public class QuangCao {

	int id;
	String ten;
	String lienket;
	String hinhanh;
	int active;

	public QuangCao(int id, String ten, String lienket, String hinhanh, int active) {
		super();
		this.id = id;
		this.ten = ten;
		this.lienket = lienket;
		this.hinhanh = hinhanh;
		this.active = active;
	}

}
